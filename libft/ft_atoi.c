/*
ОПИСАНИЕ
Функция atoi() конвертирует начальную часть строки, на которую указывает str,
в целое число. Функция ведет себя так же, как strtol(nptr, (char **)NULL, 10);
за исключением того, что она не определяет ошибок. Строка должна начинаться с
пустых символов (определяемых при помощи isspace(3)), далее должен следовать
один знак: `+' или `-' (если есть).

ВОЗВРАЩАЕМЫЕ ЗНАЧЕНИЯ
Конвертированное значение.
*/

#include "libft.h"

long long	ft_atoi(const char *str)
{
	long long	res;
	int			neg;

	res = 0;
	neg = 1;
	while ((*str == ' ') || (*str == '\n') || (*str == '\t') \
			|| (*str == '\v') || (*str == '\f') || (*str == '\r'))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + (*str - '0');
		str++;
		// if (res * neg > 0 && neg == -1)
		// 	return (0);
		// if (res * neg < 0 && neg == 1)
		// 	return (-1);
	}
	return (res * neg);
}
